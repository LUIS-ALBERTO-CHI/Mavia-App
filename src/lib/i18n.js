/**
 * Resolve a dot-notation key from a translations object.
 * e.g. get(obj, 'nav.home') → obj.nav.home
 */
function get(obj, path) {
  return path.split('.').reduce((acc, k) => (acc && acc[k] !== undefined ? acc[k] : undefined), obj);
}

/**
 * Get a translated string.
 * @param {string} lang   - Language code: 'es' | 'en' | 'fr'
 * @param {string} key    - Dot-notation key e.g. 'nav.home'
 * @param {object} params - Optional interpolation: { name: 'María' } → replaces {{name}}
 * @returns {string}
 */
export function translate(lang, key, params) {
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.es;
  let str = get(dict, key);
  // Fallback to Spanish
  if (str === undefined) str = get(TRANSLATIONS.es, key);
  // Last-resort fallback
  if (str === undefined) return key;
  // Interpolation
  if (params) {
    str = String(str).replace(/\{\{(\w+)\}\}/g, (_, k) => (params[k] !== undefined ? params[k] : `{{${k}}}`));
  }
  return String(str);
}
